"""
create a calculator capable of performing arithmetic operations on two number.
"""
num1,num2=10,20
print("1st value is ",num1)
print("2nd value is ",num2)
print("Sum(10 + 20) = ",num1+num2)
print("Subtract(10 - 20) = ",num1-num2)
print("Mul(10 * 20) = ",num1*num2)
print("Div(10 / 20) = ",num1/num2)
print("Floor Div(10 // 20) = ",num1//num2)
print("Exponential(10 ** 20) = ",num1**num2)
print("Modulus(10 % 20) = ",num1%num2)